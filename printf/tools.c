#include "printf.h"

int		is_hexa(char *str)
{
	int	i;

	i = -1;
	while (str[++i])
		if ((str[i] >= 'a' && str[i] <= 'f') \
										|| (str[i] >= 'A' && str[i] <= 'F'))
			return (1);
	return (0);
}
