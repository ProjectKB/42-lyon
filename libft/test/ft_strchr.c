#include "libft.h"
#include <stdio.h>

char	*ft_strchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return ((char *)&s[i]);
		i++;
	}
	return (NULL);
}

int main()
{
	char str[] = "hello";
	printf("%s", ft_strchr(str, 'e'));
	return (0);
}
