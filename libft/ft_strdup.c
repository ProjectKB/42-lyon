#include "./includes/libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *src)
{
	char	*copy;
	int		i;

	i = 0;
	if (!(copy = (char*)malloc(sizeof(*copy) * ft_strlen(src) + 1)))
		return (NULL);
	while (src[i])
	{
		copy[i] = src[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
