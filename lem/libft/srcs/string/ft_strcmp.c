#include "libft.h"

int ft_strcmp(const char *s2, const char *s1)
{
	size_t i;

	i = 0;
	while (s2 && s1 && s2[i] && s1[i])
	{
		if (s2[i] != s1[i])
			return (s2[i] - s1[i]);
		i++;
	}
	return (s2[i] - s1[i]);
}
