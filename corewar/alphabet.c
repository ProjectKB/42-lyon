void	print_letter(char letter, unsigned char *arena, const t_letter *alphabet, int *pos)
{
	int	i;
	int	j;
	int	line;
	int	pos_tmp;

	i = -1;
	letter < 'Q' ? letter -= 65 : (letter -= 66);
	line = 0;
	while (++i < 5 && (j = -1))
	{
		pos_tmp = *pos;
		while (++j < 5)
			arena[pos_tmp + j + (line * 64)] = alphabet[letter].l[j + (line * 5)];
		line++;
	}
	*pos += 6;
}

void	print_word(char *word, unsigned char *arena, const t_letter *alphabet)
{
	static int	pos = 65;
	int	w_start;
	int	w_size;
	int	i;
	int	len;

	i = -1;
	len = strlen(word);
	w_size = (len * 5) + len - 1;
	w_start = (64 - w_size) / 2 - 1;
	printf("len : %d start : %d size : %d\n", len, w_start, w_size);
	pos += w_start;
	while (++i < len)
		print_letter(word[i], arena, alphabet, &pos);
}

