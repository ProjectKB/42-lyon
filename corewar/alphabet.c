void	print_arena(unsigned char *arena)
{
	int	i;

	i = -1;
	while (++i < 4096)
	{
		if (!(i % 64))
			printf("\n");
		arena[i] ? printf("\033[34;01m%d\033[00m ", arena[i]) : printf("\033[34;02m%d\033[00m ", arena[i]);
	}
	printf("\n");
}

