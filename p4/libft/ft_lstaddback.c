#include "libft.h"

void	ft_lstaddback(t_list **alst, t_list *new)
{
	if (!(*alst))
		*alst = new;
	else
	{
		while ((*alst)->next)
			*alst = (*alst)->next;
		(*alst)->next = new;
	}
}
